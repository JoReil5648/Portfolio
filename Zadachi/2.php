<?php
$K = 100; // пример значения K
$dayOfWeek = ($K + 1) % 7; // вычисляем остаток от деления (K + 1) на 7
if ($dayOfWeek == 0) {
    $dayOfWeek = 7; // если остаток равен 0, то это воскресенье, поэтому присваиваем значение 7
}
echo "Номер дня недели для K-го дня года: " . $dayOfWeek;
?> 
